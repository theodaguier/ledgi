export type DashboardMessages = (typeof dashboardMessages)[AppLocale];

export function getDashboardMessages(locale: string | null | undefined): DashboardMessages {
  return dashboardMessages[normalizeAppLocale(locale)];
}

export function formatImportedRows(count: number, locale: string | null | undefined): string {
  const normalized = normalizeAppLocale(locale);

  if (normalized === "en-US") {
    return `${count} ${count === 1 ? "row" : "rows"}`;
  }

  if (normalized === "es-ES") {
    return `${count} ${count === 1 ? "fila" : "filas"}`;
  }

  return `${count} ${count === 1 ? "ligne" : "lignes"}`;
}
